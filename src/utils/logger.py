def set_color(log, color, highlight=True):
    color_set = ["white", "red", "green", "yellow", "blue", "pink", "cyan", "black"]
    try:
        index = color_set.index(color)
    except:
        index = len(color_set) - 1
    prev_log = "\033["
    if highlight:
        prev_log += "1;3"
    else:
        prev_log += "0;3"
    prev_log += str(index) + "m"
    return prev_log + log + "\033[0m"
